package programacao_Basica;

public class Piadinha {

	public static void main(String[] args) {
		
		//31 de outubro em octadecimal == 25 de dezembro em decimal
		// dias as bruxas em octa == natam em decimal
		int oct31 = 031;
		
		int dec25 = 25;
		
		
		
		System.out.println(oct31==dec25);

	}

}
